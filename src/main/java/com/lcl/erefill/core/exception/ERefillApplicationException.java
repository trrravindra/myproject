package com.lcl.erefill.core.exception;

public class ERefillApplicationException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6438679379074291040L;

	/**
	 * 
	 */

	public ERefillApplicationException() {
		super();
	}

	public ERefillApplicationException(String message, Throwable cause) {
		super(message, cause);
	}

	public ERefillApplicationException(String message) {
		super(message);
	}

	public ERefillApplicationException(Throwable cause) {
		super(cause);
	}

}
